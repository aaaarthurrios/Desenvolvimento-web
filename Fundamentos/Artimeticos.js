let a = 10;
a += 5;  // a = 15 (Equivalente a: a = a + 5)
a -= 3;  // a = 12 (Equivalente a: a = a - 3)
a *= 2;  // a = 24 (Equivalente a: a = a * 2)
a /= 4;  // a = 6 (Equivalente a: a = a / 4)
a %= 2;  // a = 0 (Equivalente a: a = a % 2)


function soma(a, b) {
    return a + b;
}
